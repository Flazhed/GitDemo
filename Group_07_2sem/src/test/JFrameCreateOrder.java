    public String[] getComboBoxCustomer() {

        ArrayList<Customer> temp = con.getAllCustomers();

        String[] orderArray = new String[temp.size()];

        for (int i = 0; i < temp.size(); i++) {
            Customer c = temp.get(i);

            orderArray[i] = c.getCustomerID() + "";

        }

        return orderArray;

    }

